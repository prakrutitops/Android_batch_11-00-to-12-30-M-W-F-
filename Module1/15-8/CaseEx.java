package com.t158;

public class CaseEx 
{
	
	public static void main(String[] args) {
		
		
		String s1="sachin";
		String s2="TOPS";
		
		System.out.println(s1.toUpperCase());
		System.out.println(s2.toLowerCase());
		
		System.out.println(s1.startsWith("sa"));
		System.out.println(s1.endsWith("n"));
		
		System.out.println(s1.charAt(1));
				
		
	}
	
}
